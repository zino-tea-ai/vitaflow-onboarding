import json
import os

# 检查 Strava 的文件名和结构化数据的键是否匹配
screens = sorted([f for f in os.listdir('projects/Strava_Analysis/Screens') if f.endswith('.png')])[:5]
print('=== Screens files (first 5) ===')
for s in screens:
    print(f'  {s}')

with open('projects/Strava_Analysis/structured_descriptions.json', 'r', encoding='utf-8') as f:
    data = json.load(f)

print()
print('=== Structured keys (first 5) ===')
keys = list(data.keys())[:5]
for k in keys:
    print(f'  {k}')

# 检查是否匹配
print()
print('=== Match check ===')
for s in screens[:3]:
    matched = s in data
    status = "MATCH" if matched else "NO MATCH"
    print(f'  {s}: {status}')



