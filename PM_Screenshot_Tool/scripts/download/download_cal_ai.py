# -*- coding: utf-8 -*-
import asyncio
import sys
import os
sys.stdout.reconfigure(encoding='utf-8')

from smart_batch_download import SmartBatchDownloader

async def download_cal_ai():
    downloader = SmartBatchDownloader()
    downloader.load_competitors()
    
    if not await downloader.init_browser():
        print("Failed to connect to browser")
        return
    
    try:
        # Create test product info
        product = {
            'app_name': 'Cal AI - Calorie Tracker',
            'publisher': 'Viral Development'
        }
        
        result = await downloader.process_product(product)
        print(f"\nResult: {result}")
        
    finally:
        await downloader.close()

if __name__ == "__main__":
    asyncio.run(download_cal_ai())


